#include "ft_printf.h"

int		ft_verif_char(int c)
{
	if (('0' <= c && c <= '9') || c == '#' || c == '0' || c == '-'
			|| c == '+' || c == ' ' || c == 'l' || c == 'j'
			|| c == 'z' || c == 'h' || c == '.' || c == 'c'
			|| c == 'C' || c == 'd' || c == 'D' || c == 'i'
			|| c == 'o' || c == 's' || c == 'S' || c == 'u'
			|| c == 'U' || c == 'O' || c == 'x' || c == 'X'
			|| c == 'p' || c == '%' || c == '*')
		return (0);
	return (1);
}
