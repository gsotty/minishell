#include "ft_printf.h"
#include <wchar.h>

int		ft_wctomb(char *s, wchar_t wc)
{
	int		bytes;

	if (!s)
		return (0);
	bytes = 0;
	if (wc > 1114111 || (wc >= 55296 && wc <= 57343))
		return (-1);
	if (wc > 65535)
		*(s + bytes++) = 240 | ((wc & 18350083) >> 18);
	if (wc > 2047)
		*(s + bytes++) = (wc <= 65535 ? 224 : 128) | ((wc & 258048) >> 12);
	if (wc > 127)
		*(s + bytes++) = (wc <= 2047 ? 192 : 128) | ((wc & 4032) >> 6);
	if (wc >= 0)
		*(s + bytes++) = (wc <= 127 ? 0 | (wc & 127) : 128 | (wc & 63));
	return (bytes);
}
