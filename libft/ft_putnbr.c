#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	int	x;

	x = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(1, "-", 1);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		x = n + 48;
		write(1, &x, 1);
	}
}
